/**
* @file sdlc.h
*/
#ifndef __SDLC_H__
#define __SDLC_H__
#include <stdio.h>
#include <stdlib.h>
#include <math.h>

/**
*  calculate square root
* @param[in] number
* @return root
*/
int squareroot(int a);


/**
*  calculate reciprocal
* @param[in] number
* @return reverse number
*/


float reciprocal(int a);

/**
*  calculate power
* @param[in] number
* @return powered value
*/


int power(int a, int b);

/**
*  calculate exponential
* @param[in] number
* @param[in] power
* @return exponential power
*/


float exponential(int a);

#endif
